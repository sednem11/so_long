#include "../so_long.h"

void	map(int fd)
{
	char	*temp;

	get()->j = 0;
	temp = NULL;
	get()->map = (char **)ft_calloc(get()->map_y + 1, sizeof(char *));
	while(get()->j < get()->map_y)
	{
		temp = get_next_line(fd);
		get()->map[get()->j] = temp;
		free(temp);
		get()->j++;
	}
}


